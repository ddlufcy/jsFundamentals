//EQUAL
'3' == 3;
console.log('3' == 3);

//JavaScript does coercion when comparing values
//Coercion is the process is the process of converting a value from one datatype to another

//STRICT EQUAL - this overrides javascript coercion
3 === 3;
console.log( '3' === 3);

//NOT EQUAL
console.log('3' != 3);

//STRICT NOT EQUAL 
'3' !== 4;

//GREATER 
3 > 2;

//LESS THAN
2 < 3;

//GREATER THAN OR EQUAL TO
3 >= 2; 

//LESS THAN OR EQUAL TO

2 <= 3

//AND
2 && 3

//OR 
2 || 3